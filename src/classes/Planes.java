package classes;

public class Planes {
    private int id;
    private java.lang.String model;
    private int seats;
    private int bathrooms;
    private int max_weight;

    public Planes(int id, String model, int seats, int bathrooms, int max_weight){
        this.id=id;
        this.model=model;
        this.seats=seats;
        this.bathrooms=bathrooms;
        this.max_weight=max_weight;
    }

}
